public class IteracionArreglo {
    public static void main(String[] args) {
        // Arrays
        int[] numeros = {10, 20, 30, 40, 50};
        // Iterar el contenido del array
        for (int i = 0; i < numeros.length; i++)
            System.out.println("El valor del indice: " + i + " = " + numeros[i]);
    }
}
