public class ReglasNombresVariables {
    public static void main(String[] args) {
        //Reglas nombre variables
        String nombreCompleto = "Juan perez";
        System.out.println("nombreCompleto = " + nombreCompleto);
        String nombrecompelto = "Juan Carlos";
        System.out.println("nombrecompelto = " + nombrecompelto);
        boolean esCasado = true;
        System.out.println("esCasado = " + esCasado);
        boolean hasSaldo = true;
        System.out.println("hasSaldo = " + hasSaldo);

        String nombreProducto = "camisa";
        System.out.println("nombreProducto = " + nombreProducto);
        float precioProducto = 14.36F;
        System.out.println("precioProducto = " + precioProducto);
        int cantidadDisponible = 20;
        System.out.println("cantidadDisponible = " + cantidadDisponible);
        boolean esDisponible = true;
        System.out.println("esDisponible = " + esDisponible);

    }
}
