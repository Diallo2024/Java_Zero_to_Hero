public class Constantes {
    public static void main(String[] args) {
        System.out.println("*** Constantes en Java ***");
        final var DIAS_SEMANA = 7;
        System.out.println("DIAS_SEMANA = " + DIAS_SEMANA);
        final var PI = 3.1416;
        System.out.println("PI = " + PI);
        System.out.println("math.PI = " + Math.PI);
        final var MANSAJE_BIENVENDIA = "Hola, bienvenido a tu curso Java";
        System.out.println("MANSAJE_BIENVENDIA = " + MANSAJE_BIENVENDIA);
        final var MINUTOS_SEGUNDO = 60;
        System.out.println("MINUTOS_SEGUNDO = " + MINUTOS_SEGUNDO);
    }
}
