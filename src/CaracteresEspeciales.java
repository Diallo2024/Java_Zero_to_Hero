public class CaracteresEspeciales {
    public static void main(String[] args) {
        //Caracteres especiales
        //\t tabulaciÃ³n
        var cadena1 = "Hola\nMundo";
        System.out.println("cadena1 = " + cadena1);

        //\' agregar une comilla simple
        var cadena2 = "Hola \' Mundo";
        System.out.println("cadena2 = " + cadena2);

        //\" agregar una comilla double
        var cadena3 = "Hola \" Mundo";
        System.out.println("cadena3 = " + cadena3);

        //agragar una barra invertida
        var cadena5 = "Hola \\ Munod";
        System.out.println("cadena5 = " + cadena5);

    }
}
