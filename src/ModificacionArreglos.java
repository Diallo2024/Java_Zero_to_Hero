public class ModificacionArreglos {
    public static void main(String[] args) {
        // Arreglos
        var enteros = new int[5];
        // Modificar los elementos
        enteros[0] = 5;
        enteros[1] = 7;
        enteros[3]= 4;
        // Lectura de valores de un arreglo
        System.out.println("El valor del índice 0 es: " + enteros[0]);
        System.out.println("El valor del ínidce 1 es: " + enteros[1]);
        System.out.println("El valor del índice 3 es: " + enteros[3]);
    }
}
